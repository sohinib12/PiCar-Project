class Car:
    def __init__(self) -> None:
        pass

    def stop(self):
        print("car stopped")

    def move_forward(self):
        print("moving forward")

    def move_backward(self):
        print("moving backward")

    def move_right(self):
        print("moving right")

    def move_left(self):
        print("moving left")

    def look_right(self):
        print("looking right")

    def look_left(self):
        print("looking left")

    def look_around(self):
        print("looking around")

    def turn_off(self):
        print("Goodbye")
